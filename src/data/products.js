// Helper function to get products by category
export const getProductsByCategory = (categoryId, subcategoryId = null) => {
  let filtered = PRODUCTS.filter(p => p.category === categoryId);
  if (subcategoryId) {
    filtered = filtered.filter(p => p.subcategory === subcategoryId);
  }
  return filtered;
};

// Helper function to get products by subcategory
export const getProductsBySubcategory = (categoryId, subcategoryId) => {
  return PRODUCTS.filter(p => p.category === categoryId && p.subcategory === subcategoryId);
};

// Get category info by id
export const getCategoryInfo = (categoryId) => {
  return CATEGORIES[categoryId] || null;
};

// Get all unique categories from products
export const getAllCategories = () => {
  return Object.values(CATEGORIES);
};

// Get subcategories for a category
export const getSubcategories = (categoryId) => {
  return CATEGORIES[categoryId]?.subcategories || {};
};

